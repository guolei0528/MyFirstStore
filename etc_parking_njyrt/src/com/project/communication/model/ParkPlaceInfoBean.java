package com.project.communication.model;

public class ParkPlaceInfoBean {
	private String type;
	private String execute_status;
	private String all_space;
	private String rest_space;
	private String fixed_car;
	private String temp_car;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getExecute_status() {
		return execute_status;
	}
	public void setExecute_status(String execute_status) {
		this.execute_status = execute_status;
	}
	public String getAll_space() {
		return all_space;
	}
	public void setAll_space(String all_space) {
		this.all_space = all_space;
	}
	public String getRest_space() {
		return rest_space;
	}
	public void setRest_space(String rest_space) {
		this.rest_space = rest_space;
	}
	public String getFixed_car() {
		return fixed_car;
	}
	public void setFixed_car(String fixed_car) {
		this.fixed_car = fixed_car;
	}
	public String getTemp_car() {
		return temp_car;
	}
	public void setTemp_car(String temp_car) {
		this.temp_car = temp_car;
	}
	
}
